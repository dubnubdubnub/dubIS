/**
 * Load real data fixtures for integration tests.
 *
 * Inventory data is loaded from pre-generated JSON (produced by
 * scripts/generate-test-fixtures.py) to guarantee parity with the
 * Python backend's _load_organized() output.
 */
import { readFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const FIXTURES = join(__dirname, '..', '..', 'fixtures');

function readFixture(name) {
  return readFileSync(join(FIXTURES, name), 'utf-8');
}

function readFixtureBytes(name) {
  return readFileSync(join(FIXTURES, name));
}

function readFixtureJSON(name) {
  return JSON.parse(readFixture(name));
}

/**
 * Load inventory from pre-generated JSON fixture.
 * Generated by: python scripts/generate-test-fixtures.py
 */
function loadInventory() {
  return readFixtureJSON('generated/inventory.json');
}

export { readFixture, readFixtureBytes, readFixtureJSON, loadInventory, FIXTURES };
